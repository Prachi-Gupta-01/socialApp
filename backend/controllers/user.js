exports.deleteMyProfile = async (req, res) => {
  try {
    if (!req.user) {
      return res.status(401).json({
        success: false,
        message: "User not authenticated",
      });
    }
    const user = await User.findByIdAndDelete(req.user._id);
    const posts = user.posts;
    await user.deleteOne();
    //logout user after deleting profile
    res.clearCookie("token", null, {
      expires: new Date(Date.now()),
      httpOnly: true,
    });

    //user k sath uski sari post hi delete ho jani chahiye
    for (let i = 0; i < user.posts.length; i++) {
      const post = await post.findById(posts[i]);
      await post.remove();
    }
    //removing user from followers list of other users
    for (let i = 0; i < user.followers.length; i++) {
      const follower = await User.findById(user.followers[i]);
      const index = follower.following.indexOf(req.user._id);
      follower.following.splice(index, 1);
      await follower.save();
    }
    //removing user from following list of other users
    for (let i = 0; i < user.following.length; i++) {
      const following = await User.findById(user.following[i]);
      const index = following.followers.indexOf(req.user._id);
      following.followers.splice(index, 1);
      await following.save();
    }
    res.status(200).json({
      success: true,
      message: "Profile deleted successfully",
    });
  } catch (error) {
    res.status(500).json({
      success: false,
      message: error.message,
    });
  }
};
